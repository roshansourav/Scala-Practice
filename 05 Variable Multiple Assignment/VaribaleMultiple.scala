object VariableMultiple{
    def main(ar : Array[String]){
        var mark1 : Int = 18;
        var mark2 : String = "mark2 variable with datatype declaration";
        println(mark1);
        println(mark2);

        var mark3 = 89;
        var mark4 = "mark4 variable without datatype declaration";
        val mark5 = 90;
        val mark6 = "mark6 variable without datatype declaration";
        println(mark3);
        println(mark4);
        println(mark5);
        println(mark6);
    }
}